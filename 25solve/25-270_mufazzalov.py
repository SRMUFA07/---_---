# промежутку [100 000 000; 200 000 000], найдите все числа,
# имеющие ровно 7 делителей, делящихся на 2022 и
# отличных от самого числа. В ответ запишите 5
# наименьших и 5 наибольших найденных чисел в порядке
# возрастания, справа от них
# укажите их максимальный делитель, отличный от самого числа.
